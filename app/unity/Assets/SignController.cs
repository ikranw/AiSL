    void Awake()
    {
        if (animator == null)
            animator = GetComponent<Animator>();

        // Auto-find input field and button if not assigned in Inspector
        if (inputField == null)
            inputField = GameObject.Find("InputField (TMP)")?.GetComponent<TMP_InputField>();

        if (submitButton == null)
        {
            var buttonObj = GameObject.Find("Button");
            if (buttonObj != null)
                submitButton = buttonObj.GetComponent<Button>();
        }

        if (submitButton != null)
            submitButton.onClick.AddListener(OnSubmitPressed);
        else
            Debug.LogWarning("[SignController] No submit button found — type Signs > Test Sign to test.");
    }

    public void OnSubmitPressed()
    {
        if (inputField == null || string.IsNullOrWhiteSpace(inputField.text))
            return;

        var tokens = new List<string>();
        foreach (var word in inputField.text.Trim().ToUpper().Split(' '))
            if (!string.IsNullOrWhiteSpace(word))
                tokens.Add(word);

        PlaySequenceFromFrontend(tokens, 1f, false);
    }

    public void PlaySequenceFromFrontend(List<string> sequence, float speed, bool loop)
    {
        if (currentRoutine != null)
            StopCoroutine(currentRoutine);

        currentSequence = sequence != null ? new List<string>(sequence) : new List<string>();
        currentSequenceIndex = 0;
        currentPlaybackSpeed = Mathf.Max(0.1f, speed);
        currentLoopPlayback = loop;
        isPaused = false;
        playbackElapsedSeconds = 0f;
        playbackTotalSeconds = EstimateSequenceDuration(currentSequence, currentPlaybackSpeed);
        currentPlaybackToken = "";
        currentPlaybackTokenIndex = -1;
        NotifyPlaybackState(true);

        currentRoutine = StartCoroutine(PlaySequenceRoutine());
    }

    public void PausePlayback()
    {
        isPaused = true;
        animator.speed = 0f;
        NotifyPlaybackState(true);
    }

    public void ResumePlayback()
    {
        isPaused = false;
        animator.speed = currentPlaybackSpeed;
        NotifyPlaybackState(true);
    }

    public void SetPlaybackSpeed(float speed)
    {
        float previousSpeed = currentPlaybackSpeed;
        currentPlaybackSpeed = Mathf.Max(0.1f, speed);

        if (playbackTotalSeconds > playbackElapsedSeconds && previousSpeed > 0f)
        {
            float remainingSeconds = playbackTotalSeconds - playbackElapsedSeconds;
            playbackTotalSeconds = playbackElapsedSeconds + (remainingSeconds * (previousSpeed / currentPlaybackSpeed));
        }

        if (!isPaused)
            animator.speed = currentPlaybackSpeed;

        NotifyPlaybackState(true);
    }

    public void SetLooping(bool loop)
    {
        currentLoopPlayback = loop;
    }

    public void ResetToIdle()
    {
        if (currentRoutine != null)
        {
            StopCoroutine(currentRoutine);
            currentRoutine = null;
        }

        currentSequence = new List<string>();
        currentSequenceIndex = 0;
        currentLoopPlayback = false;
        isPaused = false;
        playbackElapsedSeconds = 0f;
        playbackTotalSeconds = 0f;
        currentPlaybackToken = "";
        currentPlaybackTokenIndex = -1;
        ReturnToIdle();
        NotifyPlaybackState(true);
    }

    private IEnumerator PlaySequenceRoutine()
    {
        do
        {
            currentSequenceIndex = 0;
            playbackElapsedSeconds = 0f;
            NotifyPlaybackState(true);

            while (currentSequenceIndex < currentSequence.Count)
            {
                var raw = currentSequence[currentSequenceIndex];
                currentSequenceIndex++;

                if (string.IsNullOrWhiteSpace(raw))
                    continue;

                string token = raw.Trim().ToUpper();
                currentPlaybackToken = token;
                currentPlaybackTokenIndex = currentSequenceIndex - 1;
                NotifyPlaybackState(true);

                if (glossToIndex.TryGetValue(token, out int index))
                    yield return StartCoroutine(PlaySign(index));
                else
                    yield return StartCoroutine(Fingerspell(token));

                yield return StartCoroutine(WaitForPlaybackSeconds(SequenceGapSeconds));
            }
        }
        while (currentLoopPlayback);

        playbackElapsedSeconds = playbackTotalSeconds;
        currentPlaybackToken = "";
        currentPlaybackTokenIndex = -1;
        ReturnToIdle();
        currentRoutine = null;
        NotifyPlaybackState(true);
    }

    private IEnumerator PlaySign(int index)
    {
        if (!indexToStateName.ContainsKey(index))
        {
            Debug.LogWarning("No state name for index " + index);
            yield break;
        }

        animator.speed = isPaused ? 0f : currentPlaybackSpeed;
        animator.SetInteger("SignIndex", index);
        animator.ResetTrigger("PlaySign");
        animator.SetTrigger("PlaySign");

        for (int i = 0; i < StateSettleFrames; i++)
            yield return null;

        float timeout = 2f;
        float elapsed = 0f;
        string targetState = indexToStateName[index];

        while (!animator.GetCurrentAnimatorStateInfo(0).IsName(targetState) && elapsed < timeout)
        {
            elapsed += Time.deltaTime;
            yield return null;
        }

        var stateInfo = animator.GetCurrentAnimatorStateInfo(0);
        float clipLength = Mathf.Clamp(stateInfo.length, 0.3f, 4f);
        float targetPlaySeconds = Mathf.Max(0.12f, clipLength - SignEndTrimSeconds);

        float playedSeconds = 0f;
        while (playedSeconds < targetPlaySeconds)
        {
            if (!isPaused)
            {
                animator.speed = currentPlaybackSpeed;
                playbackElapsedSeconds += Time.deltaTime;
                playedSeconds += Time.deltaTime * currentPlaybackSpeed;
                NotifyPlaybackState();
            }
            else
            {
                animator.speed = 0f;
            }

            yield return null;
        }
    }

    private IEnumerator Fingerspell(string word)
    {
        foreach (char letter in word)
        {
            if (letterToIndex.TryGetValue(letter, out int index))
            {
                yield return StartCoroutine(PlaySign(index));
                yield return StartCoroutine(WaitForPlaybackSeconds(FingerspellGapSeconds));
            }
        }
    }

    private IEnumerator WaitForPlaybackSeconds(float seconds)
    {
        float elapsed = 0f;

        while (elapsed < seconds)
        {
            if (!isPaused)
            {
                playbackElapsedSeconds += Time.deltaTime;
                elapsed += Time.deltaTime * currentPlaybackSpeed;
                NotifyPlaybackState();
            }

            yield return null;
        }
    }

    private void ReturnToIdle()
    {
        isPaused = false;
        animator.speed = 1f;
        animator.SetInteger("SignIndex", 0);
        animator.ResetTrigger("PlaySign");
        animator.SetTrigger("PlaySign");
    }

    private float EstimateSequenceDuration(List<string> sequence, float speed)
    {
        float total = 0f;
        float safeSpeed = Mathf.Max(0.1f, speed);

        foreach (var raw in sequence)
        {
            if (string.IsNullOrWhiteSpace(raw))
                continue;

            string token = raw.Trim().ToUpper();
            if (glossToIndex.TryGetValue(token, out int index))
            {
                total += GetClipLengthForIndex(index) / safeSpeed;
            }
            else
            {
                foreach (char letter in token)
                {
                    if (letterToIndex.TryGetValue(letter, out int letterIndex))
                    {
                        total += GetClipLengthForIndex(letterIndex) / safeSpeed;
                        total += FingerspellGapSeconds / safeSpeed;
                    }
                }
            }

            total += SequenceGapSeconds / safeSpeed;
        }

        return total;
    }

    private float GetClipLengthForIndex(int index)
    {
        if (!indexToStateName.TryGetValue(index, out string stateName))
            return 1f;

        if (stateLengthCache.TryGetValue(stateName, out float cachedLength))
            return cachedLength;

        float clipLength = 1f;
        if (animator != null && animator.runtimeAnimatorController != null)
        {
            foreach (var clip in animator.runtimeAnimatorController.animationClips)
            {
                if (clip != null && clip.name == stateName)
                {
                    clipLength = Mathf.Clamp(clip.length, 0.3f, 4f);
                    break;
                }
            }
        }

        stateLengthCache[stateName] = clipLength;
        return clipLength;
    }

    private void NotifyPlaybackState(bool force = false)
    {
        float currentSeconds = Mathf.Clamp(playbackElapsedSeconds, 0f, playbackTotalSeconds);
        int playingFlag = currentRoutine != null && !isPaused ? 1 : 0;
        string token = currentPlaybackToken ?? "";
        int tokenIndex = currentPlaybackTokenIndex;
        int totalTokens = currentSequence != null ? currentSequence.Count : 0;

        if (!force && Mathf.Abs(currentSeconds - lastReportedPlaybackSeconds) < 0.05f && playingFlag == lastReportedIsPlaying && token == lastReportedToken && tokenIndex == lastReportedTokenIndex)
            return;

        lastReportedPlaybackSeconds = currentSeconds;
        lastReportedIsPlaying = playingFlag;
        lastReportedToken = token;
        lastReportedTokenIndex = tokenIndex;
        ReportPlaybackState(currentSeconds, playbackTotalSeconds, playingFlag, token, tokenIndex, totalTokens);
    }

